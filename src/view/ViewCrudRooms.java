    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed

        long id = Long.parseLong(txtId.getText());
        int roomNumber = Integer.parseInt(txtRoomNumber.getText());
        String typeRoom = txtTypeOfRoom.getText();
        double priceNight = Double.parseDouble(txtPriceNight.getText());
        String disponibility = txtDisponibility.getText();
        String detailsAcomodies = txtDetailsAmenities.getText();
        Room room = new Room(id, roomNumber, typeRoom, priceNight, disponibility, detailsAcomodies, (Hotel) jCBHotel.getSelectedItem());
        showTable();
        fillTable();
        clearFields();
        try {
            roomService.createRoom(room);
            JOptionPane.showMessageDialog(null, "La habitacion ha sido registrada");
        } catch (SQLException ex) {
        } catch (IdDuplicateException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnReadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReadActionPerformed
        long id = Long.parseLong(txtId.getText());
        try {
            Room room = roomService.readRoom(id);
            if (room != null) {
                txtTypeOfRoom.setText(room.getTypeOfRoom());
                txtDisponibility.setText(room.getDisponibility());
                txtDetailsAmenities.setText(room.getDetailsAmenities());
                JOptionPane.showMessageDialog(null, "Se ha encontrado la habitacion");
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo encontrar");
            }
        } catch (SQLException ex) {
        }
    }//GEN-LAST:event_btnReadActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int roomNumber = Integer.parseInt(txtRoomNumber.getText());
        String typeRoom = txtTypeOfRoom.getText();
        double priceNight = Double.parseDouble(txtPriceNight.getText());
        String disponibility = txtDisponibility.getText();
        String detailsAcomodies = txtDetailsAmenities.getText();
        long id = Long.parseLong(txtId.getText());
        Room room = new Room(id, roomNumber, typeRoom, priceNight, disponibility, detailsAcomodies, (Hotel) jCBHotel.getSelectedItem());
        clearFields();
        try {
            roomService.updateRoom(room);
            JOptionPane.showMessageDialog(null, "La habitacion ha sido actualizada");
        } catch (SQLException ex) {
        } catch (IdDuplicateException ex) {
            JOptionPane.showMessageDialog(null, "El id de la habitaciÃ³n ya esta actualizada");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed


